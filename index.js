function padString(original, length, padWith, side) {
  // –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —è–∫–∏–π –±—ñ–∫ –±—É–ª–æ –≤–∫–∞–∑–∞–Ω–æ

  // –Ø–∫—â–æ "start", –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ padStart
  // –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ –º–µ—Ç–æ–¥ padStart(), —â–æ–± –¥–æ–ø–æ–≤–Ω–∏—Ç–∏ —Ä—è–¥–æ–∫ string
  // –¥–æ –ø–µ–≤–Ω–æ—ó –¥–æ–≤–∂–∏–Ω–∏ length, –≤—Å—Ç–∞–≤–ª—è—é—á–∏ —Å–∏–º–≤–æ–ª–∏ padWith –Ω–∞ –ø–æ—á–∞—Ç–∫—É

  // –Ø–∫—â–æ "end", –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ padEnd
  // –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ –º–µ—Ç–æ–¥ padEnd(), —â–æ–± –¥–æ–ø–æ–≤–Ω–∏—Ç–∏ —Ä—è–¥–æ–∫ string
  // –¥–æ –ø–µ–≤–Ω–æ—ó –¥–æ–≤–∂–∏–Ω–∏ length, –≤—Å—Ç–∞–≤–ª—è—é—á–∏ —Å–∏–º–≤–æ–ª–∏ padWith –Ω–∞ –≤ –∫—ñ–Ω—Ü—ñ

  // –Ø–∫—â–æ –±—ñ–∫ –Ω–µ –≤–∫–∞–∑–∞–Ω–æ –∞–±–æ –≤–∫–∞–∑–∞–Ω–æ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ,
  // –ø–æ–≤–µ—Ä—Ç–∞—î–º–æ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è "Error: side should be either 'start' or 'end'"
  switch (side) {
    case "start":
      return original.padStart(length, padWith);
    case "end":
      return original.padEnd(length, padWith);
    default:
      return "Error: side should be either 'start' or 'end'";
  }
}


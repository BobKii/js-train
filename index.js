  // –Ø–∫—â–æ –∑–Ω–∞—á–µ–Ω–Ω—è –∞—Ä–≥—É–º–µ–Ω—Ç—É "side" –¥–æ—Ä—ñ–≤–Ω—é—î "both"
  // –ü–æ–≤–µ—Ä—Ç–∞—î–º–æ —Ä—è–¥–æ–∫, –≤ —è–∫–æ–º—É –≤–∏–¥–∞–ª–µ–Ω—ñ –ø—Ä–æ–±—ñ–ª–∏ –∑ –æ–±–æ—Ö —Å—Ç–æ—Ä—ñ–Ω
